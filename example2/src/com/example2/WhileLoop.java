package com.example2;

public class WhileLoop {
	
	public static void main(String[] args) {
		int a =1;
		
		while(a<=100)
		{
			if(a%2==0)
			{
				System.out.println(a + "even number");
				
			}
				if(a%2!=0)
				{
		
					System.out.println(a + "odd number");
				}
					
		
			
			a++;
			
			
			
		}
		
		
		
	}
}


